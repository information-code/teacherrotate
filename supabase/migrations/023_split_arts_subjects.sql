-- 023: 拆分藝術科任為 音樂 / 視覺藝術 / 表演藝術
--      撕榜面板本就分音樂/表藝/視藝，配課供需需對應到各別配課科目，
--      故新增三個科任職位，分數複製「藝術領域科任」(1.5)，
--      四者設同一年資群組「藝術領域」→ 跨藝術科目年資連續。
-- ============================================================

UPDATE public.scoremap SET group_name = '藝術領域' WHERE work = '藝術領域科任';

