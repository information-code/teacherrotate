  ('視覺藝術領域科任', 1.5,1.5,1.5,1.5,1.5,1.5,1.5,1.5, '藝術領域', 45),
  ('表演藝術領域科任', 1.5,1.5,1.5,1.5,1.5,1.5,1.5,1.5, '藝術領域', 45)
ON CONFLICT (work) DO NOTHING;
